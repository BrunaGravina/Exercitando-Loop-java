import java.util.Scanner;

/* Escreva um programa que leia 10 numeros que o usuario
possa digitar e imprima a media dos numeros. utilizando um
loop for;
*/
public class Main {
    public static void main(String[] args) {
        Scanner e = new Scanner(System.in);
        int cont = 0;
        int soma = 0;

        for (int i = 0; i < 10; i++) {
            System.out.println("Digite um numero: ");
            int num = e.nextInt();
            cont++;
            soma += num;
        }
        double media = soma / cont;
        System.out.println("A media Ã©: " + media);

    }
}
